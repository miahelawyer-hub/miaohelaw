/**
 * Builds src/data/tagDisplayNames.ts from oldToNew + heuristics.
 */
import fs from 'node:fs';
import { oldToNew } from './tag-old-to-new.mjs';

const inv = {};
for (const [k, v] of Object.entries(oldToNew)) {
	if (!inv[v]) inv[v] = [];
	inv[v].push(k);
}

function pickZhDisplay(slug, candidates) {
	const cjk = candidates.filter((t) => /[\u4e00-\u9fff]/.test(t));
	if (cjk.length) {
		if (slug === 'scarborough-divorce-lawyer') return '士嘉堡离婚律师';
		if (slug === 'net-family-property') return '净家庭财产';
		if (slug === 'high-net-worth-divorce-lawyer') return '高净值离婚律师';
		if (slug === 'miao-he-lawyer') return '何淼律师';
		// Prefer longer Chinese label (usually more specific)
		return cjk.sort((a, b) => b.length - a.length)[0];
	}
	const en = candidates.filter((t) => /^[a-zA-Z\s\-–—',.]+$/.test(t) && t.length > 2);
	if (en.length) return en.sort((a, b) => b.length - a.length)[0];
	return slug.replace(/-/g, ' ');
}

function pickEnDisplay(slug, candidates) {
	const ascii = candidates.filter((t) => /^[a-zA-Z0-9\s\-–—',.]+$/.test(t));
	if (ascii.length) {
		if (slug === 'miao-he-lawyer') return 'Miao He';
		if (slug === 'ontario-family-law') return 'Ontario family law';
		if (slug === 'net-family-property') return 'Net Family Property';
		return ascii.sort((a, b) => b.length - a.length)[0];
	}
	const cjk = candidates.find((t) => /[\u4e00-\u9fff]/.test(t));
	if (cjk) return pickZhDisplay(slug, candidates); // rare
	return slug.replace(/-/g, ' ');
}

const zh = {};
const en = {};
for (const slug of Object.keys(inv)) {
	const c = inv[slug];
	zh[slug] = pickZhDisplay(slug, c);
	en[slug] = pickEnDisplay(slug, c);
}

const body = `/* Auto-generated by scripts/generate-tag-display-names.mjs — fallback when tag-intro has no displayName */
export const tagDisplayNameZh: Record<string, string> = ${JSON.stringify(zh, null, '\t')};
export const tagDisplayNameEn: Record<string, string> = ${JSON.stringify(en, null, '\t')};

export function getTagDisplayNameZh(slug: string): string {
	return tagDisplayNameZh[slug] ?? slug.replace(/-/g, ' ');
}
export function getTagDisplayNameEn(slug: string): string {
	return tagDisplayNameEn[slug] ?? slug.replace(/-/g, ' ');
}
`;

fs.writeFileSync('src/data/tagDisplayNames.ts', body, 'utf8');
fs.writeFileSync(
	'scripts/tag-display-names.json',
	JSON.stringify({ zh, en }, null, 2),
	'utf8',
);
console.log('Wrote src/data/tagDisplayNames.ts', Object.keys(zh).length, 'slugs');
